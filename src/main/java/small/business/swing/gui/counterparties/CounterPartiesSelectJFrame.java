    private void svalidate() {
        CounterParties selectedObject = null;
        if (jTable.getSelectedRow() >= 0) {
            selectedObject = (CounterParties) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
        }
        counterPartiesService.setSelectedElement(selectedObject);
        counterPartiesService.validate();
        jButtonSelect.setEnabled(counterPartiesService.isCanSelect());
    }
