    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Так - Повернути товари на склад\nНі - Видалити без повернення товарів на склад", "Видалення запису", JOptionPane.YES_NO_CANCEL_OPTION);
        if (returnsOfGoodsService.isCanRemoveInvoice()) {
            if (confirm == JOptionPane.YES_OPTION) {
                ReturnsOfGoodsInvoice selectedObject = (ReturnsOfGoodsInvoice) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
                try {
                    returnsOfGoodsService.removeCurrentElement(selectedObject, true);
                } catch (Exception e) {
                    log.error("Try Remove", e);
                    JOptionPane.showMessageDialog(this, "Помилка при видаленні накладної:\nінформація про помилку знаходиться в logJ.txt", null, JOptionPane.ERROR_MESSAGE);
                }
                getList();
            }
            if (confirm == JOptionPane.NO_OPTION) {
                ReturnsOfGoodsInvoice selectedObject = (ReturnsOfGoodsInvoice) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
                try {
                    returnsOfGoodsService.removeCurrentElement(selectedObject, false);
                } catch (Exception e) {
                    log.error("Try Remove", e);
                    JOptionPane.showMessageDialog(this, "Помилка при видаленні накладної:\nінформація про помилку знаходиться в logJ.txt", null, JOptionPane.ERROR_MESSAGE);
                }
                getList();
            }
        }
    }//GEN-LAST:event_jButtonRemoveActionPerformed

