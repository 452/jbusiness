    private void getList() {
        DefaultTableModel dataModel = (DefaultTableModel) jTable.getModel();
        dataModel.setRowCount(0);
        try {
            for (ReturnsOfGoodsInvoice returnsOfGoodsInvoice : returnsOfGoodsService.getDataList()) {
                Object[] g = new Object[]{returnsOfGoodsInvoice, (returnsOfGoodsInvoice.getTypeOfReturns() == 0 ? "Повернення постачальнику" : "Повернення від клієнта"), DateFormat.getDateTimeInstance(DateFormat.FULL, DateFormat.SHORT).format(returnsOfGoodsInvoice.getReturningDate()), (returnsOfGoodsInvoice.getCounterParty() != null) ? returnsOfGoodsInvoice.getCounterParty().getTitle() : null, (returnsOfGoodsInvoice.getStoreHouse() != null) ? returnsOfGoodsInvoice.getStoreHouse() : null};
                dataModel.addRow(g);
            }
            jTable.setModel(dataModel);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Помилка при отриманні списку накладних:\nінформація про помилку знаходиться в logJ.txt", null, JOptionPane.ERROR_MESSAGE);
        }
        svalidate();
    }

