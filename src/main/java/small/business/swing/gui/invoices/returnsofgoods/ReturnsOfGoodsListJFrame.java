    private void svalidate() {
        ReturnsOfGoodsInvoice validateElement = null;
        if (jTable.getSelectedRow() >= 0) {
            validateElement = (ReturnsOfGoodsInvoice) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);

        }
        returnsOfGoodsService.setCurrentElement(validateElement);
        returnsOfGoodsService.validate();
        jButtonEdit.setEnabled(returnsOfGoodsService.isCanEditInvoice());
        jButtonRemove.setEnabled(returnsOfGoodsService.isCanRemoveInvoice());
    }

    private void edit() {
        if (jTable.getSelectedRow() >= 0) {
            ReturnsOfGoodsInvoice selectedObject = (ReturnsOfGoodsInvoice) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            returnsOfGoodsService.setCurrentElement(selectedObject);
            ReturnsElementJFrame re = new ReturnsElementJFrame();
            re.pack();
            ModalFrameUtil.showAsModal(re, this);
            getList();
        }
    }
