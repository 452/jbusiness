    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        setDataFields();
        nomenclatureService.saveUpdate();
        if (nomenclatureService.getCurrentElement().getId() == null) {
            historyService.saveActionOfAdd("Номенклатура", nomenclatureService.getCurrentElement().getTitle());
        } else {
            historyService.saveActionOfChange("Номенклатура", nomenclatureService.getCurrentElement().getTitle());
        }
        this.dispose();
    }//GEN-LAST:event_jButtonSaveActionPerformed

