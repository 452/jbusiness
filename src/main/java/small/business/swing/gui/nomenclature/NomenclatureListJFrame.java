    private void getList() {
        DefaultTableModel dataModel = (DefaultTableModel) jTable.getModel();
        dataModel.setRowCount(0);
        List<Nomenclature> dataList;
        if (isSearchMode) {
            dataList = nomenclatureService.getSearchDataList((Long) jSpinnerNomenclatureID.getValue(), jTextFieldSearchNomenclatureByTitle.getText(), jTextFieldSearchByFactorySerialArticul.getText(), jTextFieldSearchByAdditionalArticul.getText());
        } else {
            dataList = nomenclatureService.getDataList();
        }
        for (Nomenclature nomenclature : dataList) {
            Object[] g = new Object[]{(nomenclature.isGroup().equals(Nomenclature.GROUP)) ? "Група" : "-", nomenclature.getId(), nomenclature, nomenclature.getArticleofgoods(), nomenclature.getArticleinside(), nomenclature.getQuantity(), nomenclature.getQuantitymin(), nomenclature.getPrice(), nomenclature.getRetailPrice() == null ? null : (nomenclature.getRetailPrice() * settingsService.getExchangeRate()), nomenclature.getSmallWholeSalePrice() == null ? null : (nomenclature.getSmallWholeSalePrice() * settingsService.getExchangeRate()), nomenclature.getBigWholeSalePrice() == null ? null : (nomenclature.getBigWholeSalePrice() * settingsService.getExchangeRate())};
            dataModel.addRow(g);
        }
        jTable.setModel(dataModel);
        svalidate();
    }

