    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        if (evt.getClickCount() == 2) {
            Nomenclature selectedObject = (Nomenclature) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            nomenclatureService.setCurrentElement(selectedObject);
            if (selectedObject.isGroup().contentEquals(Nomenclature.GROUP)) {
                nomenclatureService.setCurrentCategory(selectedObject);
                getList();
            } else {
                NomenclatureElementJFrame nomenclatureElement = new NomenclatureElementJFrame();
                nomenclatureElement.pack();
                ModalFrameUtil.showAsModal(nomenclatureElement, this);
                getList();
            }
        }
    }//GEN-LAST:event_jTableMouseClicked

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        Nomenclature selectedObject = (Nomenclature) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
        nomenclatureService.setCurrentElement(selectedObject);
        if (selectedObject.isGroup().contentEquals(Nomenclature.GROUP)) {
            GroupElementJFrame groupElement = new GroupElementJFrame();
            groupElement.pack();
            ModalFrameUtil.showAsModal(groupElement, this);
        } else {
            NomenclatureElementJFrame nomenclatureElement = new NomenclatureElementJFrame();
            nomenclatureElement.pack();
            ModalFrameUtil.showAsModal(nomenclatureElement, this);
        }
        getList();
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Дійсно видалити запис?", "", JOptionPane.YES_NO_OPTION);
        if ((jTable.getSelectedRow() >= 0) && (confirm == JOptionPane.YES_OPTION)) {
            Nomenclature selectedObject = (Nomenclature) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            nomenclatureService.removeCurrentElement(selectedObject);
            getList();
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonMoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMoveActionPerformed
        if (jTable.getSelectedRow() >= 0) {
            nomenclatureService.setSelectType(Nomenclature.GROUP);
            NomenclatureSelectJFrame nomenclatureSelect = new NomenclatureSelectJFrame();
            nomenclatureSelect.pack();
            ModalFrameUtil.showAsModal(nomenclatureSelect, this);
            nomenclatureService.move();
            getList();
        }
    }//GEN-LAST:event_jButtonMoveActionPerformed

