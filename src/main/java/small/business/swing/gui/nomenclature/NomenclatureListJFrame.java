    private void svalidate() {
        Nomenclature validateElement = null;
        if (jTable.getSelectedRow() >= 0) {
            validateElement = (Nomenclature) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
        }
        nomenclatureService.setCurrentElement(validateElement);
        nomenclatureService.validate();
        jButtonEdit.setEnabled(nomenclatureService.isCanEdit());
        jButtonMove.setEnabled(nomenclatureService.isCanMove());
        jButtonDelete.setEnabled(nomenclatureService.isCanRemove());
    }
