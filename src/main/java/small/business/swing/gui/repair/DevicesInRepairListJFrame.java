    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        if (jTable.getSelectedRow() >= 0) {
            DeviceInRepair selectedObject = (DeviceInRepair) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            devicesInRepairService.setCurrentElement(selectedObject);
            JFrame jframe;
            if (selectedObject.isGroup().equals(DeviceInRepair.GROUP)) {
                jframe = new DevicesInRepairGroupJFrame();
            } else {
                jframe = new DevicesInRepairJFrame();
            }
            jframe.pack();
            ModalFrameUtil.showAsModal(jframe, this);
            getList();
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonMoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMoveActionPerformed
        if (jTable.getSelectedRow() >= 0) {
            devicesInRepairService.setSelectType(DeviceInRepair.GROUP);
            DevicesInRepairSelectJFrame jframe = new DevicesInRepairSelectJFrame();
            jframe.pack();
            ModalFrameUtil.showAsModal(jframe, this);
            devicesInRepairService.move();
            getList();
        }
    }//GEN-LAST:event_jButtonMoveActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null, "Дійсно видалити запис?", "", JOptionPane.YES_NO_OPTION);
        if ((jTable.getSelectedRow() >= 0) && (confirm == JOptionPane.YES_OPTION)) {
            DeviceInRepair selectedObject = (DeviceInRepair) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            devicesInRepairService.removeCurrentElement(selectedObject);
            getList();
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

