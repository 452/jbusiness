    private void svalidate() {
        DeviceInRepair validateElement = null;
        if (jTable.getSelectedRow() >= 0) {
            validateElement = (DeviceInRepair) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
        }
        devicesInRepairService.setCurrentElement(validateElement);
        devicesInRepairService.validate();
        jButtonEdit.setEnabled(devicesInRepairService.getCanEdit());
        jButtonMove.setEnabled(devicesInRepairService.getCanMove());
        jButtonDelete.setEnabled(devicesInRepairService.getCanRemove());
    }

    private void getList() {
        DefaultTableModel dataModel = (DefaultTableModel) jTable.getModel();
        dataModel.setRowCount(0);
        for (DeviceInRepair goods : devicesInRepairService.getDataList()) {
            URL image = goods.isGroup().equals(DeviceInRepair.GROUP) ? goods.getId().equals(devicesInRepairService.getCurrentCategoryId()) ? getClass().getResource("/small/business/swing/gui/images/up.png") : getClass().getResource("/small/business/swing/gui/images/group.png") : getClass().getResource("/small/business/swing/gui/images/element.png");
            Object[] g = new Object[]{image, goods};
            dataModel.addRow(g);
        }
        jTable.setModel(dataModel);
        svalidate();
    }

