    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        if (evt.getClickCount() == 2) {
            DeviceInRepair selectedObject = (DeviceInRepair) jTable.getValueAt(jTable.getSelectedRow(), OBJECT_COLUMN);
            devicesInRepairService.setCurrentElement(selectedObject);
            if (selectedObject.isGroup().contentEquals(DeviceInRepair.GROUP)) {
                devicesInRepairService.setCurrentCategory(selectedObject);
                getList();
            } else {
                DevicesInRepairJFrame jframe = new DevicesInRepairJFrame();
                jframe.pack();
                ModalFrameUtil.showAsModal(jframe, this);
                getList();
            }
        }
    }//GEN-LAST:event_jTableMouseClicked

