    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        jButtonSelect.setEnabled(false);
        if (jTable.getSelectedRow() == 0) {
            jButtonSelect.setEnabled(true);
        }
    }//GEN-LAST:event_jTableMouseClicked

    private void jButtonSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectActionPerformed
        if (jTable.getSelectedRow() >= 0) {
            StoreHouse selectedObject = (StoreHouse) jTable.getValueAt(jTable.getSelectedRow(), jTable.getSelectedColumn());
            storeHousesService.setSelectedElement(selectedObject);
            dispose();
        } else {
            jButtonSelect.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonSelectActionPerformed

    private void jTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseReleased
        jButtonSelect.setEnabled(false);
        if (jTable.getSelectedRow() == 0) {
            jButtonSelect.setEnabled(true);
        }
    }//GEN-LAST:event_jTableMouseReleased

