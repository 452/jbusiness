    private void getList() {
        DefaultTableModel dataModel = (DefaultTableModel) jTable.getModel();
        dataModel.setRowCount(0);
        for (StoreHouse storeHouse : storeHousesService.getDataList()) {
            Object[] e = new Object[]{storeHouse};
            dataModel.addRow(e);
        }
        jTable.setModel(dataModel);
        jButtonSelect.setEnabled(false);
    }

